package edu.neu.info5100.model.Objects;

public class Enterprise {
    private int id;
    private String name;

    public Enterprise(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
